			<p>Analytic Hierarchy Process (AHP) is a decision support model developed by Thomas L. Saaty. This decision support model will describe complex multi-factor or multi-criteria problems into a hierarchy. Hierarchy is defined as a representation of a complex problem in a multi-level structure where the first level is the goal, followed by the level factor, criteria, sub-criteria, and so on down to the last level of the alternative.</p>
			
			<p>AHP helps decision makers to get the best solution by decomposing complex problems into a simpler form and then synthesizing various factors involved in the decision making problem. AHP considers qualitative and quantitative aspects of a decision and reduces the complexity of a decision by making comparisons of one of the various criteria chosen to then process and obtain the results.</p>

			<p>AHP is often used as a problem solving method compared to other methods for the following reasons:</p>

